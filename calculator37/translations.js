// 현재 언어 설정
let currentCalculatorLanguage = 'ko';

// 언어 변경 함수
function changeLanguage(lang) {
    currentCalculatorLanguage = lang;
    updateCalculatorPageLanguage();
    localStorage.setItem('calculator-language', lang);
    
    // HTML lang 속성 업데이트
    document.documentElement.lang = lang;
}

// 페이지 언어 업데이트
function updateCalculatorPageLanguage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (calculatorTranslations[currentCalculatorLanguage] && calculatorTranslations[currentCalculatorLanguage][key]) {
            element.textContent = calculatorTranslations[currentCalculatorLanguage][key];
        }
    });
    
    // placeholder 업데이트
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (calculatorTranslations[currentCalculatorLanguage] && calculatorTranslations[currentCalculatorLanguage][key]) {
            element.placeholder = calculatorTranslations[currentCalculatorLanguage][key];
        }
    });
}

// 페이지 로드 시 언어 설정
document.addEventListener('DOMContentLoaded', function() {
    // 저장된 언어 설정 불러오기
    const savedLanguage = localStorage.getItem('calculator-language');
    if (savedLanguage && calculatorTranslations[savedLanguage]) {
        currentCalculatorLanguage = savedLanguage;
    } else {
        // 브라우저 언어 감지
        const browserLang = navigator.language.split('-')[0];
        if (calculatorTranslations[browserLang]) {
            currentCalculatorLanguage = browserLang;
        }
    }
    
    // 언어 선택기 업데이트
    const languageSelect = document.getElementById('language-select');
    if (languageSelect) {
        languageSelect.value = currentCalculatorLanguage;
    }
    
    // 페이지 언어 업데이트
    updateCalculatorPageLanguage();
    
    // HTML lang 속성 설정
    document.documentElement.lang = currentCalculatorLanguage;
}); 